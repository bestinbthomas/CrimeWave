package com.example.crimewave.Force;

public class Contact {
    String email;
    String telephone;
    String mobile;
    String fax;
    String web;
    String address;
    String facebook;
    String twitter;
    String youtube;
    String myspace;
    String bebo;
    String flickr;
    String forum;
    String blog;
    String rss;

    public Contact(String email, String telephone, String mobile, String fax, String web, String address, String facebook, String twitter, String youtube, String myspace, String bebo, String flickr, String forum, String blog, String rss) {
        this.email = email;
        this.telephone = telephone;
        this.mobile = mobile;
        this.fax = fax;
        this.web = web;
        this.address = address;
        this.facebook = facebook;
        this.twitter = twitter;
        this.youtube = youtube;
        this.myspace = myspace;
        this.bebo = bebo;
        this.flickr = flickr;
        this.forum = forum;
        this.blog = blog;
        this.rss = rss;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getFax() {
        return fax;
    }

    public void setFax(String fax) {
        this.fax = fax;
    }

    public String getWeb() {
        return web;
    }

    public void setWeb(String web) {
        this.web = web;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getFacebook() {
        return facebook;
    }

    public void setFacebook(String facebook) {
        this.facebook = facebook;
    }

    public String getTwitter() {
        return twitter;
    }

    public void setTwitter(String twitter) {
        this.twitter = twitter;
    }

    public String getYoutube() {
        return youtube;
    }

    public void setYoutube(String youtube) {
        this.youtube = youtube;
    }

    public String getMyspace() {
        return myspace;
    }

    public void setMyspace(String myspace) {
        this.myspace = myspace;
    }

    public String getBebo() {
        return bebo;
    }

    public void setBebo(String bebo) {
        this.bebo = bebo;
    }

    public String getFlickr() {
        return flickr;
    }

    public void setFlickr(String flickr) {
        this.flickr = flickr;
    }

    public String getForum() {
        return forum;
    }

    public void setForum(String forum) {
        this.forum = forum;
    }

    public String getBlog() {
        return blog;
    }

    public void setBlog(String blog) {
        this.blog = blog;
    }

    public String getRss() {
        return rss;
    }

    public void setRss(String rss) {
        this.rss = rss;
    }
}
